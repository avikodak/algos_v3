/****************************************************************************************************************************************************/
/*                                                           O(N^2) Algorithm                                                                       */
/****************************************************************************************************************************************************/
bool checkSubarraySum(vector<int>& userInput, int k) {
    if (userInput.size() < 2) {
        return false;
    }
    for (unsigned int outerCounter = 0; outerCounter < userInput.size() - 1; outerCounter++) {
        int sum = userInput[outerCounter];
        for (unsigned int innerCounter = outerCounter + 1; innerCounter < userInput.size(); innerCounter++) {
            sum += userInput[innerCounter];
            if (k == 0) {
                if (sum == 0) {
                    return true;
                }
            } else if (sum % k == 0) {
                return true;
            }
        }
    }
    return false;
}

