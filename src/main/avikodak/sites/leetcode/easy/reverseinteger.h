int reverse(int userInput) {
    int result = 0;
    bool isNegative = userInput < 0;
    bool isOverflowing = false;
    userInput = abs(userInput);
    while (userInput > 0) {
        if (INT_MAX / 10 < result) {
            isOverflowing = true;
            break;
        }
        result *= 10;
        if (INT_MAX - userInput % 10 < result) {
            isOverflowing = true;
            break;
        }
        result += userInput % 10;
        userInput /= 10;
    }
    if (result < 0) {
        return 0;
    } else if (isNegative) {
        return -1 * result;
    } else {
        return result;
    }
}

